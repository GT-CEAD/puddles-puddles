select	a17.ID_UG,
	a18.ID_ORGAO,
	a18.NO_ORGAO,
	a19.ID_ORGAO_MAXI
from	WD_UG_EXERCICIO	a17
	join	WD_ORGAO_EXERCICIO	a18
	  on 	a17.ID_ORGAO_UG = a18.ID_ORGAO
	join	WD_ORGAO_SUPE_EXERCICIO	a19
	  on 	a18.ID_ORGAO_SUPE = a19.ID_ORGAO_MAXI
where	(a19.ID_ORGAO_MAXI in (56000))
group by	a17.ID_UG,
	a19.ID_ORGAO_MAXI,
	a18.ID_ORGAO,
	a18.NO_ORGAO