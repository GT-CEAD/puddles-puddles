select	a11.ID_ANO_LANC  ID_ANO,
	a11.ID_MES_LANC  ID_MES,
	a11.ID_DIA_LANC  ID_DIA,
	max(a110.NO_DIA_COMPLETO)  NO_DIA_COMPLETO,
	a15.ID_ITEM_INFORMACAO  ID_ITEM_INFORMACAO,
	max(a113.NO_ITEM_INFORMACAO)  NO_ITEM_INFORMACAO,
	max(a113.CO_ITEM_INFORMACAO)  CO_ITEM_INFORMACAO,
	a16.ID_FONTE_RECURSO  ID_FONTE_RECURSO,
	max(a111.CO_FONTE_RECURSO)  CO_FONTE_RECURSO,
	max(a111.NO_FONTE_RECURSO)  NO_FONTE_RECURSO,
	a17.ID_UG  ID_UG_EXEC,
	max(a112.CO_UG)  CO_UG,
	max(a112.NO_UG)  NO_UG,
	sum(((a11.VA_MOVIMENTO_LIQUIDO * a15.IN_OPERACAO_EXPRESSAO) * a14.PE_TAXA))  SALDORITEMINFORMAO
from	WF_LANCAMENTO	a11
	join	WD_ITEM_DECODIFICADO_CCON	a15
	  on 	(a11.ID_ANO_LANC = a15.ID_ANO_ITEM_CONTA and 
	a11.ID_CONTA_CONTABIL_LANC = a15.ID_CONTA_CONTABIL)
	join	WD_MOEDA	a13
	  on 	(a11.ID_MOEDA_UG_EXEC_H = a13.ID_MOEDA)
	join	WD_TAXA_CAMBIO_MENSAL	a14
	  on 	(a13.ID_MOEDA = a14.ID_MOEDA_ORIGEM)
	join	WD_FONTE_RECURSO_DETA	a16
	  on 	(a11.ID_FONTE_RECURSO_DETA = a16.ID_FONTE_RECURSO_DETA)
	join	WD_UG_EXERCICIO	a17
	  on 	(a11.ID_ANO_LANC = a17.ID_ANO and 
	a11.ID_UG_EXEC = a17.ID_UG)
	join	WD_ORGAO_EXERCICIO	a18
	  on 	(a17.ID_ANO = a18.ID_ANO and 
	a17.ID_ORGAO_UG = a18.ID_ORGAO)
	join	WD_ORGAO_SUPE_EXERCICIO	a19
	  on 	(a18.ID_ANO = a19.ID_ANO and 
	a18.ID_ORGAO_SUPE = a19.ID_ORGAO_SUPE)
	join	WD_DIA	a110
	  on 	(a11.ID_ANO_LANC = a110.ID_ANO and 
	a11.ID_DIA_LANC = a110.ID_DIA and 
	a11.ID_MES_LANC = a110.ID_MES)
	join	WD_FONTE_RECURSO	a111
	  on 	(a16.ID_FONTE_RECURSO = a111.ID_FONTE_RECURSO)
	join	WD_UG	a112
	  on 	(a17.ID_UG = a112.ID_UG)
	join	WD_ITEM_INFORMACAO	a113
	  on 	(a15.ID_ITEM_INFORMACAO = a113.ID_ITEM_INFORMACAO)
	join	WD_MES	a114
	  on 	(a11.ID_ANO_LANC = a114.ID_ANO and 
	a11.ID_MES_LANC = a114.ID_MES)
where	(a19.ID_ORGAO_MAXI in (56000)
 and a15.ID_ITEM_INFORMACAO in (502)
 and a11.ID_ANO_LANC in (2017, 2018)
 and a114.SG_MES_COMPLETO <> '000/2018'
 and a14.ID_ANO = a11.ID_ANO_LANC
 and a14.ID_MES = a11.ID_MES_LANC)
group by	a11.ID_ANO_LANC,
	a11.ID_MES_LANC,
	a11.ID_DIA_LANC,
	a15.ID_ITEM_INFORMACAO,
	a16.ID_FONTE_RECURSO,
	a17.ID_UG